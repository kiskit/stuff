package models.tmdb;

public class IdNamePair {


	/**
	 * the genre id
	 */
	private int id;
	/**
	 * the genre name
	 */
	private String name;

	/**
	 * Default constructor 
	 */

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
